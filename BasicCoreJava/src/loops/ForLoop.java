package loops;

public class ForLoop {

	public static void main(String[] args) {
		//Initialization  //Condition     //Increment or decrement
		for(int i=1;         i<=5;              i++)
			{
			System.out.println(i);
			}

	
	for(int i=5;         i>=1;              i--)
	{
		System.out.println(i);
	}
		}
	}
	
